    public void fillTable(List<SP> dsSP) {
        tbm.setRowCount(0); // Xóa dữ liệu cũ
        for (SP sp : dsSP) {
            tbm.addRow(new Object[]{
                sp.getMacp(),
                sp.getTencp(),
                sp.getUong(),
                sp.getGia(),
                sp.getTinhtrang(),
                sp.getSoluong()
            });
        }
    }
    
    

