    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
 int result = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn thanh toán sản phẩm?");
    int[] cotdachon = tblThanhtoan.getSelectedRows();
    java.util.List<SP> selectedSPs = new java.util.ArrayList<>();
    int tongTien = 0;

    for (int row : cotdachon) {
        String macp = (String) tblThanhtoan.getValueAt(row, 0);
        String tencp = (String) tblThanhtoan.getValueAt(row, 1);
        String uong = (String) tblThanhtoan.getValueAt(row, 2);
        int gia = Integer.parseInt(tblThanhtoan.getValueAt(row, 3).toString());
        String tinhtrang = (String) tblThanhtoan.getValueAt(row, 4);
        int sl = Integer.parseInt(tblThanhtoan.getValueAt(row, 5).toString());

        if (tinhtrang.equalsIgnoreCase("CÒN")) {
            selectedSPs.add(new SP(macp, tencp, uong, gia, tinhtrang, sl));
            tongTien += gia;
        }
    }

    if (result == JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, "Tổng tiền cần thanh toán: " + tongTien + " VNĐ");
        QuanLyHoaDon qlhd = new QuanLyHoaDon(selectedSPs);
        qlhd.setVisible(true);
    } else if (result == JOptionPane.NO_OPTION) {
        JOptionPane.showMessageDialog(this, "ok");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

