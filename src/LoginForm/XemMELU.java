    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn order sản phẩm?");
        int[] cotdachon = tblMenu.getSelectedRows();
        java.util.List<SP> selectedSPs = new java.util.ArrayList<>();

    for (int row : cotdachon) {
        String macp = (String) tblMenu.getValueAt(row, 0);
        String tencp = (String) tblMenu.getValueAt(row, 1);
        String uong = (String) tblMenu.getValueAt(row, 2);
        int gia = Integer.parseInt(tblMenu.getValueAt(row, 3).toString());
        String tinhtrang = (String) tblMenu.getValueAt(row, 4);
        int sl = Integer.parseInt(tblMenu.getValueAt(row, 5).toString());

        selectedSPs.add(new SP(macp, tencp, uong, gia, tinhtrang, sl));
    }
        if (result == JOptionPane.YES_OPTION) {
            ThanhToanHD tthd = new ThanhToanHD(selectedSPs);
            tthd.setVisible(true);
        } else if (result == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "ok");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

