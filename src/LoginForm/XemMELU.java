    private void addMonMenu(String tenMon, String gia, String soLuong, String pathAnh) {
        JPanel monPanel = new JPanel();
        monPanel.setLayout(new BorderLayout(10, 10));
        Border border = BorderFactory.createLineBorder(new Color(200, 200, 200), 1);
        monPanel.setBorder(BorderFactory.createCompoundBorder(border, BorderFactory.createEmptyBorder(10, 10, 10, 10)));
        monPanel.setBackground(Color.WHITE);

        URL imgUrl = getClass().getClassLoader().getResource("images/" + pathAnh);
        if (imgUrl == null) {
            System.err.println("Không tìm thấy ảnh: " + pathAnh);
            return;
        }

        ImageIcon icon = new ImageIcon(imgUrl);
        Image img = icon.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
        JLabel lblImage = new JLabel(new ImageIcon(img));
        monPanel.add(lblImage, BorderLayout.WEST);

        JPanel infoPanel = new JPanel(new GridLayout(3, 1));
        infoPanel.setBackground(Color.WHITE);
        infoPanel.add(new JLabel("Tên món: " + tenMon));
        infoPanel.add(new JLabel("Giá: " + gia + " VNĐ"));
        infoPanel.add(new JLabel("Số lượng: " + soLuong));
        monPanel.add(infoPanel, BorderLayout.CENTER);

        panelMenu.add(monPanel);
    }
